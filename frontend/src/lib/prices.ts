export const PRICE_GROUPS = Array.from(new Set(PRICE_CATEGORIES.map((c) => c.group)));

export function findPriceBySlug(slug: string): PriceCategory | undefined {
  return PRICE_CATEGORIES.find((c) => c.slug === slug);
}

export function findPriceByName(name: string): PriceCategory | undefined {
  const lower = name.toLowerCase();
  return PRICE_CATEGORIES.find(
    (c) =>
      c.name.toLowerCase() === lower ||
      lower.includes(c.name.toLowerCase()) ||
      c.name.toLowerCase().includes(lower)
  );
}
